package day13;

public class PhysicalExamination {

	static final int VMAX = 21;
	
	static class PhyscData{
		String name;
		int height;
		double vision;
		
		PhyscData(String name, int height, double vision){
			this.name = name;
			this.height = height;
			this.vision = vision;
		}
	}
	
	static double aveHeight(PhyscData[] dat) {
		double sum = 0;
		
		for(int i=0; i<dat.length; i++) {
			sum+=dat[i].height;
		}
		return sum/dat.length;
	}
	
	public static void main(String[] args) {
		PhyscData[] x = {
				new PhyscData("박현규", 162, 0.3),
				new PhyscData("함진아", 173, 0.3),
				new PhyscData("최윤미", 175, 0.3),
				new PhyscData("홍연의", 171, 0.3),
				new PhyscData("이수진", 168, 0.3),
				new PhyscData("김영준", 174, 0.3),
				new PhyscData("박용규", 169, 0.3)
		};
		
		for(int i=0; i<x.length; i++) {
			System.out.println(x[i].name+"  "+x[i].height+"  "+x[i].vision);
		}
		
		System.out.println("평균 키 : "+aveHeight(x));
	}
	
}
